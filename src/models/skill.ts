// ---------------------------------------------------- Sequelize ------------------------------------------------------
import { Schema, model, models } from 'mongoose';
// ---------------------------------------------------------------------------------------------------------------------

const SkillSchema = new Schema({
	name: {
		type: String,
		required: true,
	},
});

const Skill = models.Skill || model('Skill', SkillSchema);

export interface ISkill {
	name: string;
}

export default Skill;
