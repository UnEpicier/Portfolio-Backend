// ---------------------------------------------------------------------------------------------------------------------

// ----------------------------------------------------- Express -------------------------------------------------------
import { Router } from 'express';
// ---------------------------------------------------------------------------------------------------------------------

// ---------------------------------------------------- Services -------------------------------------------------------
import {
	getLinks,
	createLink,
	updateLink,
	deleteLink,
} from './link.controllers';
// ---------------------------------------------------------------------------------------------------------------------

const router = Router();

router.get('/links', getLinks);

router.post('/link', createLink);

router.put('/link/:id', updateLink);

router.delete('/link/:id', deleteLink);

export default router;
